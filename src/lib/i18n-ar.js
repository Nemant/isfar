export function cityAr(airport) { return CITY_AR[airport.iata] || airport.city; }

// Arabic dual/plural for hours: ساعة / ساعتان / N ساعات / N ساعة
export function hoursAr(n) {
  const a = Math.abs(n);
  if (a === 1) return 'ساعة واحدة';
  if (a === 2) return 'ساعتين';
  if (a >= 3 && a <= 10) return `${a} ساعات`;
  return `${a} ساعة`;
}

